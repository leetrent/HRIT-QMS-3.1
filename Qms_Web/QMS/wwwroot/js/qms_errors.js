function showErrorDetails() {
    document.getElementById('errorDetails').style.display='block';
    document.getElementById('showDetailsAnchor').style.display='none';
    document.getElementById('hideDetailsAnchor').style.display='block';
}
function hideErrorDetails() {
    document.getElementById('errorDetails').style.display='none';
    document.getElementById('showDetailsAnchor').style.display='block';
    document.getElementById('hideDetailsAnchor').style.display='none';
}